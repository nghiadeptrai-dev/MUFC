const renderHistoryDetail = (historyItem) => {
  const detail = document.getElementById('history-detail');
  if (!detail) return;
  const wordCount = `${historyItem.intro} ${historyItem.content.join(' ')}`.split(/\s+/).filter(Boolean).length;
  const readTime = `${Math.max(1, Math.ceil(wordCount / 220))} phút đọc`;
  const highlights = historyItem.content.slice(0, 3).map((item) => `\n      <li>${item.replace(/\.$/, '')}</li>\n    `).join('');

  detail.innerHTML = `
    <div class="news-detail-hero">
      <div class="news-detail-visual">
        <img src="${historyItem.img}" alt="${historyItem.title}" loading="lazy" onerror="this.src='https://upload.wikimedia.org/wikipedia/en/7/7a/Manchester_United_FC_crest.svg';" />
      </div>
      <div class="news-detail-header">
        <span class="section-label">${historyItem.tag}</span>
        <h1 class="section-title">${historyItem.title}</h1>
        <div class="news-detail-meta">
          <span>Năm ${historyItem.year}</span>
          <span class="news-detail-badge">${readTime}</span>
        </div>
        <p class="news-detail-intro">${historyItem.intro}</p>
      </div>
    </div>

    <div class="news-detail-grid">
      <article class="news-detail-article">
        ${historyItem.content.map(paragraph => `<p>${paragraph}</p>`).join('')}
      </article>
      <aside class="news-detail-aside">
        <div class="news-detail-aside-card">
          <h3>Điểm nhấn nhanh</h3>
          <ul class="news-detail-keypoints">
            ${highlights}
          </ul>
        </div>
        <div class="news-detail-aside-card">
          <h3>Thông tin bài viết</h3>
          <div class="news-detail-quickinfo">
            <div><strong>Chủ đề</strong><span>${historyItem.tag}</span></div>
            <div><strong>Năm</strong><span>${historyItem.year}</span></div>
            <div><strong>Thời gian đọc</strong><span>${readTime}</span></div>
          </div>
        </div>
      </aside>
    </div>
  `;
};

const renderHistoryNotFound = () => {
  const detail = document.getElementById('history-detail');
  if (!detail) return;
  detail.innerHTML = `
    <div class="news-detail-notfound">
      <h2>Không tìm thấy mốc lịch sử</h2>
      <p>Mốc lịch sử bạn đang tìm chưa có trên trang. Vui lòng quay lại trang Thành Tích để chọn một mục khác.</p>
      <a href="history.html" class="btn btn-primary">Quay lại Thành Tích</a>
    </div>
  `;
};

const initHistoryDetailPage = () => {
  const searchParams = new URLSearchParams(window.location.search);
  const slug = searchParams.get('slug');
  if (!slug || !historyDetailData[slug]) {
    renderHistoryNotFound();
    return;
  }
  renderHistoryDetail(historyDetailData[slug]);
};

document.addEventListener('DOMContentLoaded', initHistoryDetailPage);
