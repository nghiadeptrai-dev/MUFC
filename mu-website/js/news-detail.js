const renderNewsDetail = (news) => {
  const detail = document.getElementById('news-detail');
  if (!detail) return;
  const wordCount = `${news.intro} ${news.content.join(' ')}`.split(/\s+/).filter(Boolean).length;
  const readTime = `${Math.max(1, Math.ceil(wordCount / 220))} phút đọc`;
  const highlights = news.content.slice(0, 3).map((item) => `
      <li>${item.replace(/\.$/, '')}</li>
    `).join('');

  detail.innerHTML = `
    <div class="news-detail-hero">
      <div class="news-detail-visual">
        <img src="${news.img}" alt="${news.title}" loading="lazy" onerror="this.src='https://upload.wikimedia.org/wikipedia/en/7/7a/Manchester_United_FC_crest.svg';" />
      </div>
      <div class="news-detail-header">
        <span class="section-label">${news.tag}</span>
        <h1 class="section-title">${news.title}</h1>
        <div class="news-detail-meta">
          <span>${news.date}</span>
          <span class="news-detail-badge">${readTime}</span>
        </div>
        <p class="news-detail-intro">${news.intro}</p>
      </div>
    </div>

    <div class="news-detail-grid">
      <article class="news-detail-article">
        ${news.content.map(paragraph => `<p>${paragraph}</p>`).join('')}
      </article>
      <aside class="news-detail-aside">
        <div class="news-detail-aside-card">
          <h3>Điểm nhấn nhanh</h3>
          <ul class="news-detail-keypoints">
            ${highlights}
          </ul>
        </div>
        <div class="news-detail-aside-card">
          <h3>Thông tin bài viết</h3>
          <div class="news-detail-quickinfo">
            <div><strong>Chủ đề</strong><span>${news.tag}</span></div>
            <div><strong>Ngày xuất bản</strong><span>${news.date}</span></div>
            <div><strong>Thời gian đọc</strong><span>${readTime}</span></div>
          </div>
        </div>
      </aside>
    </div>
  `;
};

const renderNewsNotFound = () => {
  const detail = document.getElementById('news-detail');
  if (!detail) return;
  detail.innerHTML = `
    <div class="news-detail-notfound">
      <h2>Không tìm thấy bài viết</h2>
      <p>Bài viết bạn đang tìm chưa có trên trang. Vui lòng quay lại trang tin tức để chọn bài khác.</p>
      <a href="news.html" class="btn btn-primary">Quay lại Tin Tức</a>
    </div>
  `;
};

const initNewsDetailPage = () => {
  const searchParams = new URLSearchParams(window.location.search);
  const slug = searchParams.get('slug');
  if (!slug || !newsDetailData[slug]) {
    renderNewsNotFound();
    return;
  }
  renderNewsDetail(newsDetailData[slug]);
};

document.addEventListener('DOMContentLoaded', initNewsDetailPage);
